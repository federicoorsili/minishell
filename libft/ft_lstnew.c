#include "libft.h"

t_list		*ft_lstnew(void *content)
{
	t_list	*new_ele;

	if ((new_ele = (t_list *)malloc(sizeof(t_list))) == NULL)
		return (NULL);
	new_ele->content = content;
	new_ele->next = NULL;
	return (new_ele);
}
