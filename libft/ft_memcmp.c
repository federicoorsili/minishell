#include "libft.h"

int						ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*p_s1;
	unsigned char	*p_s2;

	p_s1 = (unsigned char *)s1;
	p_s2 = (unsigned char *)s2;
	while (n > 1 && *p_s1 == *p_s2)
	{
		p_s1++;
		p_s2++;
		n--;
	}
	if (!n)
		return (0);
	return ((int)(*p_s1 - *p_s2));
}
