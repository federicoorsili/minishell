#include "libft.h"

char	*ft_strdup(char *s1)
{
	size_t	size;
	char	*dup;
	int		i;

	size = ft_strlen(s1);
	if (!(dup = ft_calloc(size + 1, sizeof(char))))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = 0;
	return (dup);
}
